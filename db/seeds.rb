Common Questions
==========================
Q1. The address looks like it is not in the voting area.
A1. Voters can specify a different mailing address when they register to vote. We honor that request and mail to where they have asked even if it is not in the voting area. Use the address provided.

Q2. The address is obviously incomplete.
A2. Skip it. Do not email us. We will not be able to research it and correct it. That's the way it is in the voting data file from the state.

Q3. There is a duplicate address.
A3. Skip it. Do not email us. Sometimes a duplicate will slip by. We do not want you to send two postcards to the same address.

Q4. I went back for more addresses for the same candidate and they aren't in the menu any longer.
A4. That means we don't have any addresses for that campaign. Do not email us.

Q5. I wrote extra postcards for a campaign and now they aren't listed any longer.
A5. When we assign the last address, we remove the campaign from the menu. That means we don't have any more addresses. Please try not to write cards in advance without addresses assigned to you first.

Q6. I have a great idea of something different to write on the cards that you didn't mention.
A6. Please stick to the items mentioned above which have been approved. Do not add any facts, websites, phone numbers, instructions, slogans, warnings, polling place information, other election dates or deadlines, or any information about registering to vote or verifying registration. If it is not listed as an approved item to write, do not write it.

Q7. I can't write my list of addresses after all.
A7. Email the list number to Postcards@TonyTheDemocrat.org and will reassign the whole list. There is no way to reassign partially completed lists.

Q8. I write with a group. How do I sign up if I want to be able to write on my own in between group events?
A8. Take a picture of one of your written postcards and email it to us at: Join@TonyTheDemocrat.org

Any other questions? Email: Postcards@TonyTheDemocrat.org


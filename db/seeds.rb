Read the Rules
==========================
As a volunteer writer for Postcards to Voters, you agree to follow these rules:

1. Handwrite your postcards legibly. Your message including the voter address must be written by hand each time on each card.
2. Use cards that are inclusive. If you buy, print, or make postcards, the images/graphics/photos and wording/quotes/slogans should be issue-neutral/benign/safe and not depict any holiday.
3. Mail completed postcards daily and complete them as quickly as possible by the listed deadline to allow your assigned voters time to plan to vote.
4. Only sign your first name or initials. Do not include your return address, email address, last name, or social media handle or account name.
5. Only write to each voter once. Do not contact the voters again for any reason.
6. Cover addresses before taking photos. If you share digital photos of your cards on social media, always cover voters' addresses.
7. Keep cards looking grassroots-like. Do not print logos/graphics/images/photos belonging to the candidate or campaign on the cards. Do not scan one handwritten card and reproduce by machine.
8. Stick to the approved message items provided. Do not add new facts/instructions/warnings/anything not listed as an approved message item.
9. Ask us if you have questions: Postcards@TonyTheDemocrat.org
10. Have fun!

